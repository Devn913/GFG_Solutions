class Solution {
    int lps(String pat) {
        // code here
        int M = pat.length();
        int[] lps = new int[M];
        // Length of the previous longest prefix suffix
        int len = 0;
      
        // lps[0] is always 0
        lps[0] = 0;

        // Loop calculates lps[i] for i = 1 to M-1
        int i = 1;
        while (i < M) {
            if (pat.charAt(i) == pat.charAt(len)) {
                len++;
                lps[i] = len;
                i++;
            }
            else {
                if (len != 0) {
                    len = lps[len - 1];
                }
                else {
                    lps[i] = 0;
                    i++;
                }
            }
        }
        return lps[M - 1];
        
    }
}