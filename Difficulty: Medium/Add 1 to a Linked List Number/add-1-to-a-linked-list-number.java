class Solution {
    public Node reverse(Node head){
        Node prev = null;
        Node curr = head;
        Node next = head.next;
        while(next!=null){
            curr.next = prev;
            prev = curr;
            curr = next;
            next = next.next;
        }
        curr.next = prev;
        return curr;
    }
    public Node addOne(Node head) {
        if(head == null) return new Node(1);
        // code here.
        head = reverse(head);
        Node temp = head;
        int carry = 1;
        while(carry != 0 && temp!=null){
            int val = temp.data + carry;
            carry = val/10;
            temp.data = val%10;
            if(temp.next == null && carry!=0){
                temp.next = new Node(carry);
                return reverse(head);
            }
            temp = temp.next;
            
        }
        return reverse(head);
    }
}
